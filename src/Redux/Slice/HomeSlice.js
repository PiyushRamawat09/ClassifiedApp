const homeSlice = createSlice({
  name: 'homeData',
  initialState,
  reducers: {
    likeDislike: (state, action) => {
      const {arrayName, index} = action.payload;
      if (state.homeData[arrayName] && state.homeData[arrayName][index]) {
        state.homeData[arrayName][index].save =
          !state.homeData[arrayName][index].save;
      }
    },
  },
});

export const {likeDislike} = homeSlice.actions;

export default homeSlice.reducer;
